//ADD AND RENDER CART
//1. Render Element Cart
const renderElementCart = () =>{
    if(!cart.length){
        $(".widget-shopping-cart").css("display", "none");
        $(".cart-total").css("display", "none");
        $(".cart-button").css("display", "none");
        $(".no-product").css("display", "block");

    }else{
        $(".widget-shopping-cart").css("display", "block");
        $(".cart-total").css("display", "flex");
        $(".cart-button").css("display", "block");
        $(".no-product").css("display", "none");
    }
}
renderElementCart()
//1. Reset Cart
const resetCart = () => {
    $(".cart-list").empty();
    $(".total-price").empty();
}
//3. Render Cart
const renderCart = cart => {
    let total = 0;
    cart.forEach(item => {
        $(".cart-list").append(`
            <li class="flex  j-between">
            <div class="left flex">
                <a href="" class="img-product">
                    <img src="${item.image[0]}" alt="">
                </a>
                <div class="info-product">
                    <a href="" class="name-product">${item.name}</a>
                    <div class="quanity  flex a-center">
                    <p class="count">${item.quantity}</p>
                    <p class="divider">X</p>
                    <p class="price">$<span class="price-render">${item.price}</span>.00</p>
                    </div>
                </div>
            </div>
            <div class="right">
                <div data-id=${item.id} class="btn-remove-from-cart">
                <i class="far fa-times"></i>
                </div>
            </div>
            </li>
        `);
        total = total + ((item.quantity)*(item.price));
        $(".total-price").text(total);
    });
};
//4. Click button Add Cart
$(document).on("click", ".btn-add-cart", function () {
    const id = $(this).closest(".product-id").data("id");
    const existInCart = cart.findIndex(val => val.id === id);
    const item = products.find(val => val.id === id);
    var inputQuantity = $(this).closest(".varication-add-to-cart").find("input").val();
    if(existInCart === -1){
        item.quantity = 1;
        cart.push(item);
    }else{
        cart[existInCart].quantity = item.quantity + 1;
    }

    //render no of items
    $(".nos").text(cart.length);
    renderElementCart()
    resetCart();
    renderCart(cart);
});
//4. Remove Cart
$(document).on("click",".btn-remove-from-cart", function () {
    const idCart = $(this).data('id')
    const idDel = cart.findIndex(val => val.id === idCart)
    cart.splice(idDel,1)

    $(".nos").text(cart.length);
    renderElementCart()
    resetCart();
    renderCart(cart);
});

